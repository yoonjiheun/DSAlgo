package dsalgo.node;

public class BinaryNode extends Node {
	BinaryNode left;
	BinaryNode right;

	public BinaryNode getLeft() {
		return left;
	}

	public void setLeft(BinaryNode left) {
		this.left = left;
	}

	public void setRight(BinaryNode right) {
		this.right = right;
	}

	public BinaryNode getRight() {
		return right;
	}
}
